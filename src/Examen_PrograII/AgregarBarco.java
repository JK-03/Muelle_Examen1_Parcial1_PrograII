    private void BotonAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonAgregarMouseClicked
        String tipoBarco = TiposBarcoBox.getSelectedItem().toString();
        String nombre = "";
        
        if (tipoBarco.equals("Pesquero")) {
            nombre = NombrePesqueroField.getText().toUpperCase();
            Main_Muelle.muelle.setTipoCarga(TipoCargaBox.getSelectedItem().toString().toUpperCase());
        } else if (tipoBarco.equals("Pasajero")) {
            nombre = NombrePasajeroField.getText().toUpperCase();
            int limitePasajeros = Integer.parseInt(LimitePasajerosField.getText());
            double precioBoleto = Double.parseDouble(PrecioBoletosField.getText());
            Main_Muelle.muelle.setLimitePasajeros(limitePasajeros);
            Main_Muelle.muelle.setPrecioBoleto(precioBoleto);
        }
        
        Main_Muelle.muelle.setNombreAgregar(nombre);
        Main_Muelle.muelle.agregarBarco(tipoBarco);
        
        MenuPrincipal menuPrincipal = new MenuPrincipal();
        menuPrincipal.setVisible(true);
        this.setVisible(false);
        
    }//GEN-LAST:event_BotonAgregarMouseClicked

